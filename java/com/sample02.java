public class Main {
 
    public static void main(String[] args) throws Exception {
 
        int age = 25;
 
        // 条件分岐処理
        if (age > 20) {
            System.out.println("あなたの年齢は20歳以上ですね！");
        }
        else {
            System.out.println("あなたの年齢は20歳以上ではありません");
        }
    }
}
